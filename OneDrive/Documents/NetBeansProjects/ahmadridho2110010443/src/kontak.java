public class kontak {
    
    private String id, nama, email, subjek, pesan;
    
    public kontak () {}
    
    public void setIdKontak(String id){
    this.id=id;
    }
    
    public String getIdKontak(){
    return this.id;
    }
    
    public void setNamaKontak(String nama){
    this.nama=nama;
    }
    
    public String getNamaKontak(){
    return this.nama;
    }
    
    public void setEmail(String email){
    this.email=email;
    }
    
    public String getEmail(){
    return this.email;
    }
    
    public void setSubjek(String subjek){
    this.subjek=subjek;
    }
    
    public String getSubjek(){
    return this.subjek;
    }
    
    public void setPesan(String pesan){
    this.pesan=pesan;
    }
    
    public String getPesan(){
    return this.pesan;
    }
    
}
