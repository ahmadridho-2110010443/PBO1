public class pesanan {
    
    private String id, tanggal, nama, alamat;
    private String kota, ongkir, telepon, status;
    
    public pesanan () {}
    
    public void setIdPesanan(String id){
    this.id=id;
    }
    
    public String getIdPesanan(){
    return this.id;
    }
    
    public void setTanggalPesanan(String tanggal){
    this.tanggal=tanggal;
    }
    
    public String getTanggalPesanan(){
    return this.tanggal;
    }
    
    public void setNamaPesanan(String nama){
    this.nama=nama;
    }
    
    public String getNamaPesanan(){
    return this.nama;
    }
    
    public void setAlamat(String alamat){
    this.alamat=alamat;
    }
    
    public String getAlamat(){
    return this.alamat;
    }
    
    public void setKota(String kota){
    this.kota=kota;
    }
    
    public String getKota(){
    return this.kota;
    }
    
     public void setOngkir(String ongkir){
    this.ongkir=ongkir;
    }
    
    public String getOngkir(){
    return this.ongkir;
    }
    
     public void setTelepon(String telepon){
    this.telepon=telepon;
    }
    
    public String getTelepon(){
    return this.telepon;
    }
    
     public void setStatus(String status){
    this.status=status;
    }
    
    public String getStatus(){
    return this.status;
    }
    
}
